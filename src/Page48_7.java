public class Page48_7 {
    public static void main(String[] args) {

        // Wrong
        System.out.println("25 / 4 is " + 25 / 4);
        // Right
        System.out.println("25 / 4.0 is " + 25 / 4.0);
        // Wrong
        System.out.println("3 * 2 / 4 is " + 3 * 2 / 4);
        // Right
        System.out.println("3.0 * 2 / 4 is " + 3.0 * 2 / 4);
    }
}